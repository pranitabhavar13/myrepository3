package com.operatordemo;

public class Relational {
	
	public static void main(String[] args) {
		
		//create variables
		int a=7,b=11;
		//value of a & b
		
		System.out.println("Value of a is:" +a+ " & b is:" +b);
		//== operator
		System.out.println(a==b);
		//!= operator
		System.out.println(a !=b);
		//> operator
		System.out.println(a>b);
		//< operator
		System.out.println(a<b);
		//>= operator
		System.out.println(a>=b);
		//<= operator
		System.out.println(a<=b);
		
	}

}
